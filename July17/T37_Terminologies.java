package July17;
//Java Tutorial: Basic Terminologies in Object Oriented Programming
// OOPs terminology

// Abstraction -> Hiding internal details (show only essential info!)
// Example: We use phone without bothering about how it was made.

// Encapsulation -> The act of putting various components together (in a capsule)
// Example: laptop is a single entity wit hwifi + speaker + storage in a single box!

// In java capsulation simply means that the sensitive data can be hidden from the users

// Inheritance: The act of deriving new things from existing things.
// Rickshaw => E-Rickshaw
// Phone => SmartPhone                   -- implements DRY!

// Polymorphism: One entity many forms
// SmartPhone -> Phone
// SmartPhone -> Calculator
// SmartPhone -> Camera
// SmartPhone -> Music player

